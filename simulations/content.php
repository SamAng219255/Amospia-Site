<p>Game of Life is a "zero player game" where you set an initial state by activating or deactivating "cells" and then it progresses without influence. It was originally created by the mathematician <a href="https://en.wikipedia.org/wiki/John_Horton_Conway">John Horton Conway</a>. I recreated it here based on its simple rules:<p>
<ul>
	<li>Cells with two adjacent active cells retain their state.</li>
	<li>Cells with three adjacent active cells are set to active.</li>
	<li>All other cells are set to inactive.</li>
</ul>
<br>
<p>Gravity is a simulation of a gravitational system.</p>
<p>You create bodies of certain sizes, masses, and starting velocities and allow them to move only dictated on the principles of gravity.</p>